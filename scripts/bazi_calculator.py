def _read_json_input(raw: str) -> dict:
    """Parse JSON input, stripping any UTF-8 BOM."""
    raw = raw.strip()
    # Strip BOM if present (common on Windows / PowerShell)
    if raw.startswith('﻿'):
        raw = raw[1:]
    if not raw:
        raise ValueError("No input provided")
    return json.loads(raw)


def main() -> None:
    """Main entry point. Supports CLI flags, positional args, and stdin JSON.

    CLI flags:  python bazi_calculator.py --year 1990 --month 6 --day 15 --hour 12 [--minute 0] [--gender male]
    Positional: python bazi_calculator.py 1990 6 15 12 [0] [male]
    Stdin:      echo '{"year":1990,...}' | python bazi_calculator.py
    File:       python bazi_calculator.py --file input.json
    """

    parser = argparse.ArgumentParser(
        description="Ba Zi (Four Pillars of Destiny) calculator",
        epilog="Without flags, reads JSON from stdin. "
               "With positional numeric args: <year> <month> <day> <hour> [minute] [gender].",
    )
    parser.add_argument("--year",   "-y", type=int, help="Birth year (e.g. 1990)")
    parser.add_argument("--month",  "-m", type=int, help="Birth month (1-12)")
    parser.add_argument("--day",    "-d", type=int, help="Birth day (1-31)")
    parser.add_argument("--hour",   "-H", type=int, help="Birth hour (0-23)")
    parser.add_argument("--minute", "-M", type=int, default=0, help="Birth minute (0-59, default 0)")
    parser.add_argument("--gender", "-g", type=str, default="male",
                        help="Gender: male/female or 男/女 (default male)")
    parser.add_argument("--file",   "-f", type=str, help="Read JSON input from file")

    # Also accept positional args for backward compatibility
    parser.add_argument("positional", nargs="*", help=argparse.SUPPRESS)

    args = parser.parse_args()

    # Determine input mode
    if args.year is not None:
        # Named-flag mode
        year = args.year
        month = args.month
        day = args.day
        hour = args.hour
        minute = args.minute
        gender_raw = args.gender

        # Validate required fields in flag mode
        if month is None or day is None or hour is None:
            parser.error("--year, --month, --day, --hour are all required in flag mode")
    elif args.file:
        # File input mode
        try:
            with open(args.file, 'r', encoding='utf-8-sig') as f:
                raw = f.read()
        except FileNotFoundError:
            print(json.dumps({"error": f"File not found: {args.file}"}, ensure_ascii=False))
            sys.exit(1)
        except OSError as e:
            print(json.dumps({"error": f"Cannot read file: {e}"}, ensure_ascii=False))
            sys.exit(1)
        try:
            data = _read_json_input(raw)
        except (json.JSONDecodeError, ValueError) as e:
            print(json.dumps({"error": f"Invalid JSON in file: {e}"}, ensure_ascii=False))
            sys.exit(1)
        return _from_data(data)
    elif args.positional and args.positional[0].isdigit():
        # Positional-arg mode (backward compat)
        try:
            pos = args.positional
            year = int(pos[0])
            month = int(pos[1])
            day = int(pos[2])
            hour = int(pos[3])
            minute = int(pos[4]) if len(pos) > 4 and pos[4].isdigit() else 0
            gender_raw = pos[5] if len(pos) > 5 else "male"
        except (IndexError, ValueError):
            print(json.dumps(
                {"error": "Usage: python bazi_calculator.py <year> <month> <day> <hour> [minute] [gender]"},
                ensure_ascii=False))
            sys.exit(1)
    else:
        # Stdin JSON mode — read binary, then decode to handle BOM
        raw_bytes = sys.stdin.buffer.read()
        raw = raw_bytes.decode('utf-8-sig')
        try:
            data = _read_json_input(raw)
        except (json.JSONDecodeError, ValueError) as e:
            print(json.dumps({"error": f"Invalid JSON: {e}"}, ensure_ascii=False))
            sys.exit(1)
        return _from_data(data)

    # Normalize gender
    gender = gender_raw.lower()
    if gender not in ("male", "female", "男", "女"):
        gender = "male"
    if gender == "男":
        gender = "male"
    elif gender == "女":
        gender = "female"

    # Validate date
    try:
        date(year, month, day)
    except ValueError as e:
        print(json.dumps({"error": f"Invalid date: {e}"}, ensure_ascii=False))
        sys.exit(1)

    result = format_chart_output(year, month, day, hour, minute, gender)
    print(json.dumps(result, ensure_ascii=False, indent=2))


def _from_data(data: dict) -> None:
    """Process chart from already-parsed JSON data dict."""
    required = ["year", "month", "day", "hour", "gender"]
    missing = [k for k in required if k not in data]
    if missing:
        print(json.dumps(
            {"error": f"Missing required fields: {', '.join(missing)}"},
            ensure_ascii=False))
        sys.exit(1)

    year = int(data["year"])
    month = int(data["month"])
    day = int(data["day"])
    hour = int(data["hour"])
    minute = int(data.get("minute", 0))
    gender_raw = str(data["gender"]).lower()

    gender = gender_raw
    if gender not in ("male", "female", "男", "女"):
        gender = "male"
    if gender == "男":
        gender = "male"
    elif gender == "女":
        gender = "female"

    try:
        date(year, month, day)
    except ValueError as e:
        print(json.dumps({"error": f"Invalid date: {e}"}, ensure_ascii=False))
        sys.exit(1)

    result = format_chart_output(year, month, day, hour, minute, gender)
    print(json.dumps(result, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
