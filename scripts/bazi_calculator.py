def get_minggong(year_stem_idx: int, month_branch_idx: int, hour_branch_idx: int) -> tuple[int, int]:
    """Compute 命宫 (Life Palace) stem and branch.
    命宫地支 = (month_branch - hour_branch - 1) mod 12 (正月起寅, 顺至生月, 逆至生时)
    命宫天干 from 五虎遁 based on year stem.
    """
    mg_branch = (month_branch_idx - hour_branch_idx - 1) % 12
    first_month_stem = FIVE_TIGER[year_stem_idx]
    mg_stem = (first_month_stem + mg_branch - 2) % 10  # 2 = 寅
    return mg_stem, mg_branch


def get_taiyuan(month_stem_idx: int, month_branch_idx: int) -> tuple[int, int]:
    """Compute 胎元 (Fetus Origin) stem and branch.
    胎元 = month stem + 1, month branch + 3.
    """
    ty_stem = (month_stem_idx + 1) % 10
    ty_branch = (month_branch_idx + 3) % 12
    return ty_stem, ty_branch


def get_dayun(year_stem_idx: int, month_stem_idx: int, month_branch_idx: int,
              birth_date: date, prev_jie_date: date, next_jie_date: date, gender: str) -> tuple[list[dict], int, int]:
    """Compute major luck cycles (大运)."""
    # Normalize gender (handle Chinese characters)
    g = str(gender).lower()
    if g == "男":
        g = "male"
    elif g == "女":
        g = "female"
    year_is_yang = STEM_YY[year_stem_idx] == "阳"
    is_male = g == "male"

    go_forward = (year_is_yang and is_male) or (not year_is_yang and not is_male)

    # Calculate starting age
    if go_forward:
        target_date = next_jie_date
    else:
        target_date = prev_jie_date

    days_diff = abs((birth_date - target_date).days)
    start_age = round(days_diff / 3.0)

    dayun_list = []
    for i in range(8):
        age = start_age + i * 10
        if go_forward:
            dy_stem = (month_stem_idx + 1 + i) % 10
            dy_branch = (month_branch_idx + 1 + i) % 12
            direction = "顺排"
        else:
            dy_stem = (month_stem_idx - 1 - i) % 10
            dy_branch = (month_branch_idx - 1 - i) % 12
            direction = "逆排"

        dayun_list.append({
            "start_age": age,
            "stem": STEMS[dy_stem],
            "stem_idx": dy_stem,
            "branch": BRANCHES[dy_branch],
            "branch_idx": dy_branch,
            "nayin": get_nayin(dy_stem, dy_branch),
            "direction": direction,
        })

    return dayun_list, start_age, days_diff


