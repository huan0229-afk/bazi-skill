def get_wuxing_counts(day_stem_idx: int, pillars_data: list[tuple[str, int, int]]) -> dict[str, int]:
    """Count five elements across all pillars (stems + branches)."""
    counts = {"木": 0, "火": 0, "土": 0, "金": 0, "水": 0}

    # Day stem
    counts[ELEMENTS[day_stem_idx]] += 1

    # Each pillar stem and branch
    for pname, s_idx, b_idx in pillars_data:
        counts[ELEMENTS[s_idx]] += 1
        counts[BRANCH_ELEMENTS[b_idx]] += 1

    return counts


def format_chart_output(year: int, month: int, day: int, hour: int, minute: int, gender: str) -> dict:
    """Main calculation and formatting function."""
    y_s, y_b = get_year_pillar_for_date(year, month, day, hour, minute)
    d_s, d_b = get_day_pillar(year, month, day)
    m_s, m_b, prev_jie, next_jie = get_month_pillar(year, month, day, y_s, hour, minute)
    h_s, h_b = get_hour_pillar(d_s, hour, minute)
    birth_date = date(year, month, day)

    pillars = [
        ("year", y_s, y_b),
        ("month", m_s, m_b),
        ("day", d_s, d_b),
        ("hour", h_s, h_b),
    ]

    # Hidden stems for each branch
    hidden_info = {}
    for pname, s_idx, b_idx in pillars:
        hs_list = get_hidden_stems_info(b_idx)
        hidden_info[pname] = [
            {"stem": STEMS[hs[0]], "stem_idx": hs[0], "ten_god": get_ten_god(d_s, hs[0]),
             "strength": hs[1]}
            for hs in hs_list
        ]

    # Ten gods for stems and branch本气
    ten_gods = {}
    for pname, s_idx, b_idx in pillars:
        main_hidden = get_hidden_stems_info(b_idx)[0][0] if get_hidden_stems_info(b_idx) else b_idx
        ten_gods[pname] = {
            "stem_tg": get_ten_god(d_s, s_idx),
            "branch_tg": get_ten_god(d_s, main_hidden),
            "branch_tg_stem": STEMS[main_hidden],
        }

    # Five elements
    five_elems = {}
    for pname, s_idx, b_idx in pillars:
        five_elems[pname] = {
            "stem": ELEMENTS[s_idx],
            "branch": BRANCH_ELEMENTS[b_idx],
        }

    # Na Yin
    nayins = {}
    for pname, s_idx, b_idx in pillars:
        nayins[pname] = get_nayin(s_idx, b_idx)

    # Wuxing counts
    wuxing_counts = get_wuxing_counts(d_s, pillars)

    # Major luck
    prev_jie_date = prev_jie[1]
    next_jie_date = next_jie[1]
    dayun, start_age_rounded, start_age_days = get_dayun(
        y_s, m_s, m_b, birth_date, prev_jie_date, next_jie_date, gender)

    # Shensha
    shensha = get_shensha(pillars, d_s, y_s, y_b)

    # Current year liunian (current year ± 1)
    current_year = date.today().year
    liunian_list = []
    for yr in [current_year - 1, current_year, current_year + 1]:
        ln_s, ln_b = get_year_pillar(yr)
        ln_cycle = NAYIN_IDX_MAP.get((ln_s, ln_b), -1)
        liunian_list.append({
            "year": yr,
            "stem": STEMS[ln_s],
            "stem_idx": ln_s,
            "branch": BRANCHES[ln_b],
            "branch_idx": ln_b,
            "nayin": NAYIN[ln_cycle] if ln_cycle >= 0 else "未知",
            "ten_god": get_ten_god(d_s, ln_s),
            "sixty_cycle_idx": ln_cycle,
        })

    # 命宫 and 胎元
    mg_s, mg_b = get_minggong(y_s, m_b, h_b)
    ty_s, ty_b = get_taiyuan(m_s, m_b)
    minggong = {
        "stem": STEMS[mg_s], "stem_idx": mg_s,
        "branch": BRANCHES[mg_b], "branch_idx": mg_b,
        "nayin": get_nayin(mg_s, mg_b),
        "ten_god": get_ten_god(d_s, mg_s),
    }
    taiyuan = {
        "stem": STEMS[ty_s], "stem_idx": ty_s,
        "branch": BRANCHES[ty_b], "branch_idx": ty_b,
        "nayin": get_nayin(ty_s, ty_b),
        "ten_god": get_ten_god(d_s, ty_s),
    }

    return {
        "birth": {
            "year": year, "month": month, "day": day,
            "hour": hour, "minute": minute, "gender": gender,
            "shichen": BRANCHES[h_b] + "时",
        },
        "pillars": {
            "year": {"stem": STEMS[y_s], "stem_idx": y_s, "branch": BRANCHES[y_b],
                     "branch_idx": y_b, "nayin": nayins["year"],
                     "xunkong": (y_xk := get_xunkong(NAYIN_IDX_MAP.get((y_s, y_b), 0))),
                     "is_xunkong": y_b in y_xk[2]},
            "month": {"stem": STEMS[m_s], "stem_idx": m_s, "branch": BRANCHES[m_b],
                      "branch_idx": m_b, "nayin": nayins["month"],
                      "xunkong": (m_xk := get_xunkong(NAYIN_IDX_MAP.get((m_s, m_b), 0))),
                      "is_xunkong": m_b in m_xk[2]},
            "day": {"stem": STEMS[d_s], "stem_idx": d_s, "branch": BRANCHES[d_b],
                    "branch_idx": d_b, "nayin": nayins["day"],
                    "xunkong": (d_xk := get_xunkong(NAYIN_IDX_MAP.get((d_s, d_b), 0))),
                    "is_xunkong": d_b in d_xk[2]},
            "hour": {"stem": STEMS[h_s], "stem_idx": h_s, "branch": BRANCHES[h_b],
                     "branch_idx": h_b, "nayin": nayins["hour"],
                     "xunkong": (h_xk := get_xunkong(NAYIN_IDX_MAP.get((h_s, h_b), 0))),
                     "is_xunkong": h_b in h_xk[2]},
        },
        "day_master": {
            "stem": STEMS[d_s], "stem_idx": d_s,
            "element": ELEMENTS[d_s], "yin_yang": STEM_YY[d_s],
        },
        "ten_gods": ten_gods,
        "five_elements": five_elems,
        "hidden_stems": hidden_info,
        "wuxing_counts": wuxing_counts,
        "dayun": dayun,
        "start_age": start_age_rounded,
        "start_age_days": start_age_days,
        "shensha": shensha,
        "solar_terms": {
            "prev_jie": prev_jie[0],
            "next_jie": next_jie[0],
            "prev_jie_date": str(prev_jie_date),
            "next_jie_date": str(next_jie_date),
            "prev_jie_time": f"{prev_jie[2]:02d}:{prev_jie[3]:02d}",
            "next_jie_time": f"{next_jie[2]:02d}:{next_jie[3]:02d}",
        },
        "sixty_cycle_idx": {
            "year": NAYIN_IDX_MAP.get((y_s, y_b), -1),
            "month": NAYIN_IDX_MAP.get((m_s, m_b), -1),
            "day": NAYIN_IDX_MAP.get((d_s, d_b), -1),
            "hour": NAYIN_IDX_MAP.get((h_s, h_b), -1),
        },
        "boundary_warning": _check_boundary(birth_date, prev_jie, next_jie),
        "liunian": liunian_list,
        "minggong": minggong,
        "taiyuan": taiyuan,
    }


def _check_boundary(birth_date: date, prev_jie: tuple, next_jie: tuple) -> Optional[str]:
    """Check if birth is near a solar term boundary (within 1 day).

    Since we now compare exact hour/minute against the solar term time,
    same-day births are correctly assigned. The warning is for verification.
    """
    prev_jie_date = prev_jie[1]
    next_jie_date = next_jie[1]
    prev_diff = abs((birth_date - prev_jie_date).days)
    next_diff = abs((birth_date - next_jie_date).days)

    if prev_diff <= 1 or next_diff <= 1:
        jie_name = next_jie[0] if next_diff <= prev_diff else prev_jie[0]
        jie_time = f"{next_jie[2]:02d}:{next_jie[3]:02d}" if next_diff <= prev_diff else f"{prev_jie[2]:02d}:{prev_jie[3]:02d}"
        return ("出生日期接近「{}」节气交界（{}天，节气时刻约 {}），月柱已按精确时辰计算，"
                "建议核实。".format(jie_name, min(prev_diff, next_diff), jie_time))
    return None


