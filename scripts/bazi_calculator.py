def get_hour_pillar(day_stem_idx, hour, minute=0):
    """Compute hour pillar (时柱)."""
    # Determine 时辰 branch
    total_minutes = hour * 60 + minute

    if total_minutes >= 1380 or total_minutes < 60:  # 23:00 - 01:00
        h_branch = 0  # 子
    elif total_minutes < 180:  # 01:00 - 03:00
        h_branch = 1  # 丑
    elif total_minutes < 300:  # 03:00 - 05:00
        h_branch = 2  # 寅
    elif total_minutes < 420:  # 05:00 - 07:00
        h_branch = 3  # 卯
    elif total_minutes < 540:  # 07:00 - 09:00
        h_branch = 4  # 辰
    elif total_minutes < 660:  # 09:00 - 11:00
        h_branch = 5  # 巳
    elif total_minutes < 780:  # 11:00 - 13:00
        h_branch = 6  # 午
    elif total_minutes < 900:  # 13:00 - 15:00
        h_branch = 7  # 未
    elif total_minutes < 1020:  # 15:00 - 17:00
        h_branch = 8  # 申
    elif total_minutes < 1140:  # 17:00 - 19:00
        h_branch = 9  # 酉
    elif total_minutes < 1260:  # 19:00 - 21:00
        h_branch = 10  # 戌
    else:  # 21:00 - 23:00
        h_branch = 11  # 亥

    # Hour stem from 五鼠遁
    first_hour_stem = FIVE_RAT[day_stem_idx]
    h_stem = (first_hour_stem + h_branch) % 10

    return h_stem, h_branch


def get_nayin(stem_idx, branch_idx):
    """Get 纳音 for a stem-branch pair."""
    cycle_idx = NAYIN_IDX_MAP.get((stem_idx, branch_idx))
    if cycle_idx is None:
        return "未知"
    return NAYIN[cycle_idx]


def get_ten_god(day_stem_idx, other_stem_idx):
    """Compute ten god (十神) name for other_stem relative to day_stem."""
    d_elem = ELEMENTS[day_stem_idx]
    o_elem = ELEMENTS[other_stem_idx]
    same_yy = STEM_YY[day_stem_idx] == STEM_YY[other_stem_idx]

    if day_stem_idx == other_stem_idx:
        return "比肩"

    if d_elem == o_elem:
        return "劫财" if not same_yy else "比肩"

    # day generates other
    if GEN_BY[o_elem] == d_elem:
        return "食神" if same_yy else "伤官"

    # other generates day
    if GEN_BY[d_elem] == o_elem:
        return "偏印" if same_yy else "正印"

    # day controls other
    if CTRL_BY[o_elem] == d_elem:
        return "偏财" if same_yy else "正财"

    # other controls day
    if CTRL_BY[d_elem] == o_elem:
        return "七杀" if same_yy else "正官"

    return "未知"


