def main():
    try:
        raw = sys.stdin.read()
        if not raw.strip():
            print(json.dumps({"error": "No input provided"}, ensure_ascii=False))
            sys.exit(1)
        data = json.loads(raw)
    except json.JSONDecodeError as e:
        print(json.dumps({"error": f"Invalid JSON: {e}"}, ensure_ascii=False))
        sys.exit(1)

    required = ["year", "month", "day", "hour", "gender"]
    missing = [k for k in required if k not in data]
    if missing:
        print(json.dumps(
            {"error": f"Missing required fields: {', '.join(missing)}"},
            ensure_ascii=False))
        sys.exit(1)

    year = int(data["year"])
    month = int(data["month"])
    day = int(data["day"])
    hour = int(data["hour"])
    minute = int(data.get("minute", 0))
    gender = str(data["gender"]).lower()

    if gender not in ("male", "female", "男", "女"):
        gender = "male"  # default

    if gender == "男":
        gender = "male"
    elif gender == "女":
        gender = "female"

    # Validate date
    try:
        date(year, month, day)
    except ValueError as e:
        print(json.dumps({"error": f"Invalid date: {e}"}, ensure_ascii=False))
        sys.exit(1)

    result = format_chart_output(year, month, day, hour, minute, gender)
    print(json.dumps(result, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
